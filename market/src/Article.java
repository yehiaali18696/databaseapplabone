public class Article {
    public Integer id;
    public String name;
    public Double price;
    public Integer quantity;
    public String unit;

    // Constructor
    public Article(Integer id, String name, Double price, Integer quantity, String unit) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.unit = unit;
    }

    
}
